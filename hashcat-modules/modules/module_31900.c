typedef struct cake_wallet
{
  u32 target_hash160[5]; // 20 bytes = 160 bits
  u8  target_address[64]; // Bech32 address for reference

} cake_wallet_t;

typedef struct cake_wallet_tmp
{
  u64 dgst[8]; // For PBKDF2-HMAC-SHA512
  u64 out[8];

} cake_wallet_tmp_t;

